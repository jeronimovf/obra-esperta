package br.com.obraesperta.maven.si.converters;

import br.com.obraesperta.maven.si.entities.ColaboradorCarreiraX;
import br.com.obraesperta.maven.si.controllers.ColaboradorCarreiraXFacade;
import br.com.obraesperta.maven.si.beans.util.JsfUtil;
import java.util.logging.Level;
import java.util.logging.Logger;
import javax.inject.Inject;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import javax.faces.convert.FacesConverter;

@FacesConverter(value = "colaboradorCarreiraXConverter")
public class ColaboradorCarreiraXConverter implements Converter {

    @Inject
    private ColaboradorCarreiraXFacade ejbFacade;

    @Override
    public Object getAsObject(FacesContext facesContext, UIComponent component, String value) {
        if (value == null || value.length() == 0 || JsfUtil.isDummySelectItem(component, value)) {
            return null;
        }
        return this.ejbFacade.find(getKey(value));
    }

    java.lang.Integer getKey(String value) {
        java.lang.Integer key;
        key = Integer.valueOf(value);
        return key;
    }

    String getStringKey(java.lang.Integer value) {
        StringBuffer sb = new StringBuffer();
        sb.append(value);
        return sb.toString();
    }

    @Override
    public String getAsString(FacesContext facesContext, UIComponent component, Object object) {
        if (object == null
                || (object instanceof String && ((String) object).length() == 0)) {
            return null;
        }
        if (object instanceof ColaboradorCarreiraX) {
            ColaboradorCarreiraX o = (ColaboradorCarreiraX) object;
            return getStringKey(o.getId());
        } else {
            Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, "object {0} is of type {1}; expected type: {2}", new Object[]{object, object.getClass().getName(), ColaboradorCarreiraX.class.getName()});
            return null;
        }
    }

}
